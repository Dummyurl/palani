<script type="text/javascript">
	$(document).ready(function(){
     $('[data-toggle!="collapse"]').on('click', function () {
        var classname = $(this).data('toggle');
        var parent = $(this).data('target');
        parent = parent ? $(parent) : $(this);
        var has = $(parent).hasClass(classname);
        has ? $(parent).removeClass(classname) : $(parent).addClass(classname);
        var expand = $(this).data('expand');
        var expandparent = expand ? $(expand) : null;
        if (expandparent) {
            $(expandparent).find('[data-toggle="collapse"]').data('parent', '');
            $(expandparent).find('.collapse').collapse(has ? "hide" : "show");
            if (has) {
                $(expandparent).find('[data-toggle="collapse"]').data('parent', expand);
            }
        }
    });
 });
